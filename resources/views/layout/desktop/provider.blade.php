<script>
    document.addEventListener('DOMContentLoaded', function() {
        const providerSlide = document.getElementById('slots_providers');
        const mainContent = providerSlide.querySelector('main');
        const leftTrigger = providerSlide.querySelector('.left_trigger');
        const rightTrigger = providerSlide.querySelector('.right_trigger');

        // Menentukan jumlah geser dalam piksel
        const slideAmount = 300; // Sesuaikan sesuai kebutuhan

        // Mengatur event listener untuk geser ke kiri
        leftTrigger.addEventListener('click', function() {
            mainContent.scrollLeft -= slideAmount;
        });

        // Mengatur event listener untuk geser ke kanan
        rightTrigger.addEventListener('click', function() {
            mainContent.scrollLeft += slideAmount;
        });
    });
</script>
